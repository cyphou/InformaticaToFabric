# ─────────────────────────────────────────────

def generate_cicd(config=None, fmt=None, output_dir=None):
    """Generate all CI/CD pipeline definitions.

    Args:
        config: Migration config dict.
        fmt: Output format (github/azdo/all).
        output_dir: Output base directory.

    Returns:
        dict with generated file paths and validation results.
    """
    config = config or _load_config()
    fmt = fmt or "all"
    result = {"github": None, "azdo": None, "secrets": [], "validation": {}}

    result["secrets"] = extract_secrets(config)

    if fmt in ("github", "all"):
        content, path = generate_github_actions(config, output_dir)
        valid, issues = validate_github_actions(content)
        result["github"] = path
        result["validation"]["github"] = {"valid": valid, "issues": issues}

    if fmt in ("azdo", "all"):
        content, path = generate_azure_devops(config, output_dir)
        valid, issues = validate_azure_devops(content)
        result["azdo"] = path
        result["validation"]["azdo"] = {"valid": valid, "issues": issues}

    return result


def main():
    parser = argparse.ArgumentParser(description="CI/CD Pipeline Generator")
    parser.add_argument("--format", choices=["github", "azdo", "all"], default="all")
    parser.add_argument("--config", type=str, default=None)
    parser.add_argument("--output-dir", type=str, default=None)
    args = parser.parse_args()

    config = _load_config(args.config)
    result = generate_cicd(config=config, fmt=args.format, output_dir=args.output_dir)

    print(f"\n  CI/CD Pipeline Generation Complete")
    print(f"  ──────────────────────────────────")
    if result["github"]:
        v = result["validation"].get("github", {})
        status = "valid" if v.get("valid") else f"issues: {v.get('issues', [])}"
        print(f"  GitHub Actions: {result['github']} ({status})")
    if result["azdo"]:
        v = result["validation"].get("azdo", {})
        status = "valid" if v.get("valid") else f"issues: {v.get('issues', [])}"
        print(f"  Azure DevOps:   {result['azdo']} ({status})")
    print(f"  Secrets required: {len(result['secrets'])}")
    for s in result["secrets"]:
        req = " (required)" if s.get("required") else ""
        print(f"    → {s['name']}: {s['description']}{req}")
    print()


if __name__ == "__main__":
    main()
