def validate_github_actions(content):
    """Validate GitHub Actions YAML structure. Returns (valid, issues)."""
    issues = []

    # Check required top-level keys
    for key in ["name:", "on:", "jobs:"]:
        if key not in content:
            issues.append(f"Missing required key: {key}")

    # Check for hardcoded secrets (security check)
    import re
    if re.search(r'(password|token|secret|key)\s*[:=]\s*["\'][^${}]+["\']', content, re.IGNORECASE):
        issues.append("Potential hardcoded secret detected")

    # Check stages exist
    for stage in ["assess:", "convert:", "validate:", "deploy:"]:
        if stage not in content:
            issues.append(f"Missing stage: {stage}")

    return len(issues) == 0, issues


def validate_azure_devops(content):
    """Validate Azure DevOps pipeline YAML structure. Returns (valid, issues)."""
    issues = []

    for key in ["trigger:", "stages:"]:
        if key not in content:
            issues.append(f"Missing required key: {key}")

    # Check for hardcoded secrets
    import re
    if re.search(r'(password|token|secret|key)\s*[:=]\s*["\'][^${}]+["\']', content, re.IGNORECASE):
        issues.append("Potential hardcoded secret detected")

    return len(issues) == 0, issues


