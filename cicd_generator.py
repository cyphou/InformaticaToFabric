def generate_github_actions(config, output_dir=None):
    """Generate .github/workflows/migration.yml.

    Returns:
        str: YAML content for the GitHub Actions workflow.
    """
    target = config.get("target", "fabric")
    secrets = extract_secrets(config)

    # Build secret references
    secret_env_lines = []
    for s in secrets:
        secret_env_lines.append(f"          {s['env_var']}: ${{{{ secrets.{s['name']} }}}}")
    secret_env_block = "\n".join(secret_env_lines)

    deploy_step = ""
    if target in ("fabric", "all"):
        deploy_step = """\
      - name: Deploy to Fabric
        if: github.ref == 'refs/heads/main'
        env:
          FABRIC_WORKSPACE_ID: ${{ secrets.FABRIC_WORKSPACE_ID }}
          FABRIC_TOKEN: ${{ secrets.FABRIC_TOKEN }}
        run: python deploy_to_fabric.py --dry-run"""
    else:
        deploy_step = """\
      - name: Deploy to Databricks
        if: github.ref == 'refs/heads/main'
        env:
          DATABRICKS_HOST: ${{ secrets.DATABRICKS_HOST }}
          DATABRICKS_TOKEN: ${{ secrets.DATABRICKS_TOKEN }}
        run: python deploy_to_databricks.py --dry-run"""

    workflow = f"""\
# Auto-generated by cicd_generator.py — Sprint 94
# Generated: {datetime.now(timezone.utc).isoformat()}
#
# Informatica → {'Fabric' if target == 'fabric' else 'Databricks'} Migration Pipeline
# Stages: Assess → Convert → Validate → Deploy

name: Migration Pipeline

on:
  push:
    branches: [main, develop]
    paths:
      - 'input/**'
      - 'migration.yaml'
      - 'run_*.py'
  pull_request:
    branches: [main]
  workflow_dispatch:
    inputs:
      target:
        description: 'Target platform'
        required: false
        default: '{target}'
        type: choice
        options:
          - fabric
          - databricks
          - dbt
          - auto
      dry_run:
        description: 'Dry run (no deployment)'
        required: false
        default: true
        type: boolean

permissions:
  contents: read
  id-token: write

env:
  PYTHON_VERSION: '3.12'
  INFORMATICA_MIGRATION_TARGET: ${{{{ github.event.inputs.target || '{target}' }}}}

jobs:
  assess:
    name: "\U0001F50D Assessment"
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}
          cache: pip

      - name: Install dependencies
        run: pip install -e ".[all]"

      - name: Run assessment
        run: python run_assessment.py

      - name: Upload inventory
        uses: actions/upload-artifact@v4
        with:
          name: inventory
          path: output/inventory/

  convert:
    name: "\u2699\uFE0F Conversion"
    needs: assess
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}
          cache: pip

      - name: Install dependencies
        run: pip install -e ".[all]"

      - name: Download inventory
        uses: actions/download-artifact@v4
        with:
          name: inventory
          path: output/inventory/

      - name: Convert SQL
        run: python run_sql_migration.py

      - name: Generate notebooks
        run: python run_notebook_migration.py

      - name: Generate pipelines
        run: python run_pipeline_migration.py

      - name: Generate schema
        run: python run_schema_generator.py

      - name: Upload artifacts
        uses: actions/upload-artifact@v4
        with:
          name: migration-output
          path: |
            output/sql/
            output/notebooks/
            output/pipelines/
            output/schema/

  validate:
    name: "\u2705 Validation"
    needs: convert
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}
          cache: pip

      - name: Install dependencies
        run: pip install -e ".[all]"

      - name: Download artifacts
        uses: actions/download-artifact@v4
        with:
          name: migration-output
          path: output/

      - name: Download inventory
        uses: actions/download-artifact@v4
        with:
          name: inventory
          path: output/inventory/

      - name: Run validation
        run: python run_validation.py

      - name: Run artifact validation
        run: python run_artifact_validation.py

      - name: Run tests
        run: pytest tests/ -x --tb=short

      - name: Upload validation results
        uses: actions/upload-artifact@v4
        with:
          name: validation
          path: output/validation/

  deploy:
    name: "\U0001F680 Deploy"
    needs: validate
    if: github.ref == 'refs/heads/main' && github.event.inputs.dry_run != 'true'
    runs-on: ubuntu-latest
    environment: production
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}
          cache: pip

      - name: Install dependencies
        run: pip install -e ".[all]"

      - name: Download all artifacts
        uses: actions/download-artifact@v4
        with:
          name: migration-output
          path: output/

      - name: Azure Login
        uses: azure/login@v2
        with:
          creds: ${{{{ secrets.AZURE_CREDENTIALS }}}}

      {deploy_step}

      - name: Upload deployment log
        uses: actions/upload-artifact@v4
        with:
          name: deployment-log
          path: output/deployment_log.json
"""

    if output_dir:
        out = Path(output_dir)
    else:
        out = OUTPUT_DIR
    gh_dir = out / "github"
    gh_dir.mkdir(parents=True, exist_ok=True)
    path = gh_dir / "migration.yml"
    path.write_text(workflow, encoding="utf-8")
    return workflow, str(path)


