import logging
import os
from datetime import datetime, timezone
from pathlib import Path

logger = logging.getLogger(__name__)

WORKSPACE = Path(__file__).resolve().parent
OUTPUT_DIR = WORKSPACE / "output" / "cicd"


def _load_config(config_path=None):
    """Load migration.yaml config."""
    if config_path:
        p = Path(config_path)
    else:
        p = WORKSPACE / "migration.yaml"
    if not p.exists():
        return {}
    try:
        import yaml
        with open(p, encoding="utf-8") as f:
            return yaml.safe_load(f) or {}
    except ImportError:
        return {}


# ─────────────────────────────────────────────
#  Secret Mapping
# ─────────────────────────────────────────────

def extract_secrets(config):
    """Extract required secrets from config for CI/CD secret injection.

    Returns:
        list of dicts with name, description, env_var, and source metadata.
    """
    secrets = []
    target = config.get("target", "fabric")

    # Common secrets
    secrets.append({
        "name": "AZURE_CREDENTIALS",
        "description": "Azure service principal credentials (JSON)",
        "env_var": "AZURE_CREDENTIALS",
        "required": True,
    })

    if target in ("fabric", "all"):
        secrets.append({
            "name": "FABRIC_WORKSPACE_ID",
            "description": "Microsoft Fabric workspace GUID",
            "env_var": "FABRIC_WORKSPACE_ID",
            "required": True,
        })
        secrets.append({
            "name": "FABRIC_TOKEN",
            "description": "Fabric API bearer token",
            "env_var": "FABRIC_TOKEN",
            "required": True,
        })

    if target in ("databricks", "dbt", "pyspark", "auto", "all"):
        secrets.append({
            "name": "DATABRICKS_HOST",
            "description": "Databricks workspace URL",
            "env_var": "DATABRICKS_HOST",
            "required": True,
        })
        secrets.append({
            "name": "DATABRICKS_TOKEN",
            "description": "Databricks personal access token",
            "env_var": "DATABRICKS_TOKEN",
            "required": True,
        })
        catalog = config.get("databricks", {}).get("catalog", "main")
        secrets.append({
            "name": "DATABRICKS_CATALOG",
            "description": f"Unity Catalog name (default: {catalog})",
            "env_var": "INFORMATICA_DATABRICKS_CATALOG",
            "required": False,
        })

    return secrets


